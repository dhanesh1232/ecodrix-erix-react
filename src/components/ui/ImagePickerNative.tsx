// ─── Helpers ────────────────────────────────────────────────────────────────

const formatBytes = (bytes: number | undefined, decimals = 1) => {
  if (!bytes || !+bytes) return "0 B";
  const k = 1024;
  const dm = decimals < 0 ? 0 : decimals;
  const sizes = ["B", "KB", "MB", "GB", "TB"];
  const i = Math.floor(Math.log(bytes) / Math.log(k));
  return `${parseFloat((bytes / k ** i).toFixed(dm))} ${sizes[i]}`;
};

const getDisplayName = (src: ImageFormat) => {
  let name = src.fileName || "";
  if (!name && src.url) {
    name = decodeURIComponent(src.url.split("/").pop() || "");
  }
  if (!name) return "";
  // Strip trailing timestamp appended by storage (e.g. somefile_1712046892123.jpg)
  name = name.replace(/[_-]\d{13,}(\.[^.]+)?$/, (_, p1) => p1 || "");
  const dotIdx = name.lastIndexOf(".");
  const nameWithoutExt = dotIdx > 0 ? name.slice(0, dotIdx) : name;
  const ext = dotIdx > 0 ? name.slice(dotIdx + 1) : "";
  const cleanName = nameWithoutExt.replace(/[-_]/g, " ").trim();
  return ext ? `${cleanName}.${ext}` : cleanName;
};

/**
 * Checks whether a backend media item is an image.
 * Checks categorical type first (backend now returns "image" | "video" | "audio" | "document"),
 * then falls back to MIME type and URL extension for legacy items.
 */
const isImage = (src: ImageFormat) => {
  if (src.type === "image") return true; // backend categorical type
  if (src.type?.startsWith("image/")) return true; // legacy MIME type
  if (src.url?.match(/\.(jpe?g|png|gif|webp|avif|svg)$/i)) return true;
  if (src.fileName?.match(/\.(jpe?g|png|gif|webp|avif|svg)$/i)) return true;
  return false;
};

const isPdf = (src: ImageFormat) => {
  if (src.type === "document") return true; // categorical — all docs treated as pdf filter
  if (src.type === "application/pdf") return true; // legacy MIME type
  if (src.url?.match(/\.pdf$/i)) return true;
  if (src.fileName?.match(/\.pdf$/i)) return true;
  return false;
};

const isVideo = (src: ImageFormat) => {
  if (src.type === "video") return true; // backend categorical type
  if (src.type?.startsWith("video/")) return true; // legacy MIME type
  if (src.url?.match(/\.(mp4|webm|ogg|mov)$/i)) return true;
  if (src.fileName?.match(/\.(mp4|webm|ogg|mov)$/i)) return true;
  return false;
};

const isAudio = (src: ImageFormat) => {
  if (src.type === "audio") return true;
  if (src.type?.startsWith("audio/")) return true;
  if (src.url?.match(/\.(mp3|ogg|wav|aac|flac|m4a|opus)$/i)) return true;
  if (src.fileName?.match(/\.(mp3|ogg|wav|aac|flac|m4a|opus)$/i)) return true;
  return false;
};

